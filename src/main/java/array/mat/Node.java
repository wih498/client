package array.mat;

public class Node {
    int i, j;
    char v;

    public int getI() {
        return i;
    }

    public Node setI(int i) {
        this.i = i;
        return this;
    }

    public int getJ() {
        return j;
    }

    public Node setJ(int j) {
        this.j = j;
        return this;
    }

    public char getV() {
        return v;
    }

    public Node setV(char v) {
        this.v = v;
        return this;
    }
}
